package day02_EscapeSequences;

/*
1. Create a class named Face.java
2. Write a code to print the following output in the
console:

 +"""""+
[| O O |]
 |  ^  |
 | '-' |
 +-----+

 */
public class Face {
    public static void main(String[] args) {
        System.out.println(" +\"\"\"\"\"+");
        System.out.println("[| O O |]");
        System.out.println(" |  ^  |");
        System.out.println(" | '-' |");
        System.out.println(" +-----+");
    }

}
