package day02_EscapeSequences;

/*
1. Create a class named Diamond1.java
2. Write a code to print the following output in the
console:
      ^
     / \
    /   \
   /     \
  /       \
  _________
  \       /
   \     /
    \   /
     \ /
      V
 */

public class Diamond1 {
    public static void main(String[] args) {
        System.out.println("     ^");
        System.out.println("    / \\");
        System.out.println("   /   \\");
        System.out.println("  /     \\");
        System.out.println(" /       \\");
        System.out.println(" _________");
        System.out.println(" \\       /");
        System.out.println("  \\     /");
        System.out.println("   \\   /");
        System.out.println("    \\ /");
        System.out.println("     V");
    }
}
